/*06.create a trigger to restrict user form using the table on sunday.*/
  
  CREATE OR REPLACE TRIGGER TR_HOLIDAY
  BEFORE INSERT OR UPDATE OR DELETE ON EMP
  FOR EACH ROW
  
  BEGIN
	IF TRIM(TO_CHAR(SYSDATE,'DAY'))= 'SUNDAY' THEN
		RAISE_APPLICATION_ERROR(-20420,'YOU CAN NOT MODIFY DATA ON HOLIDAY');
  END;
  /